function buildHookSnippet(i, [name, desc, lines, signature]) {
  const prefix = `srehook${pad(i)}`;
  const body = [
    "import { useCallback, useEffect, useRef, useState } from \"react\";",
    "",
    `export function ${name}${signature} {`,
    ...lines.map((l) => `  ${l}`),
    "}",
    "$0",
  ];
  return {
    key: `Shelb React Hook — ${name} (${prefix})`,
    snippet: { prefix, description: `${desc}.`, body },
  };
}

