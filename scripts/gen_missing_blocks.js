/**
 * Build a snippet record from a hand-crafted body factory.
 * The factory receives a context object and returns the body string[] WITHOUT
 * the trailing $0 cursor anchor; this helper appends it.
 */
function buildRichSnippet({ family, idx, label, role, description, factory }) {
  const prefix = `${family}${pad(idx)}`;
  const name = `${family}${pascal(label)}${pad(idx)}`;
  const title = `Shelb E-com ${role} — ${label} (${prefix})`;
  const body = factory({ component: name, label, prefix, role });
  return {
    key: title,
    snippet: {
      prefix,
      description,
      body: [...body, "$0"],
    },
  };
}

function buildFamily({ family, role, labelMap }) {
  const out = {};
  labelMap.forEach((entry, i) => {
    const { key, snippet } = buildRichSnippet({
      family,
      idx: i + 1,
      label: entry.label,
      role,
      description: entry.description,
      factory: entry.factory,
    });
    out[key] = snippet;
  });
  return out;
}

