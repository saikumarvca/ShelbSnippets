function pascal(words) {
  return words
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((w) => w[0].toUpperCase() + w.slice(1))
    .join("");
}

function buildEcomSnippet({ prefix, family, idx, label, role, body }) {
  const name = `${family}${pascal(label)}${pad(idx)}`;
  const title = `Shelb E-com ${role} — ${label} (${prefix})`;
  return {
    key: title,
    snippet: {
      prefix,
      description: `${role} variant ${pad(idx)} (${label}).`,
      body: [
        "import React from \"react\";",
        "",
        ...body(name, label),
        "$0",
      ],
    },
  };
}

