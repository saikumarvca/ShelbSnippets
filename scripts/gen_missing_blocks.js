function pickProductBody(family) {
  if (family === "seproduct" || family === "sepcard") return ecomCardBody;
  return ecomGenericBody;
}

function makeProductGroup({ family, role, labels }) {
  const out = {};
  labels.forEach((label, i) => {
    const idx = i + 1;
    const prefix = `${family}${pad(idx)}`;
    const builder = pickProductBody(family);
    const { key, snippet } = buildEcomSnippet({
      prefix,
      family,
      idx,
      label,
      role,
      body: (component) => builder(component, label, role),
    });
    out[key] = snippet;
  });
  return out;
}

