/**
 * One-shot generator for the Shelb numbered snippet prefix groups that
 * live alongside the legacy `ecommerce.code-snippets` namespace.
 *
 * Generates:
 *   - snippets/react-hooks-blocks.code-snippets       (srehook01..20)
 *   - snippets/ecommerce-product-blocks.code-snippets (seproduct01..20, sepcard01..20)
 *   - snippets/ecommerce-checkout-blocks.code-snippets(secart01..20, secheckout01..20, seorder01..20)
 *   - snippets/ecommerce-admin-blocks.code-snippets   (seaccount01..20, seadmin01..20, sevendor01..20)
 *
 * Bodies are designed to be drop-in, useful React + TypeScript + Tailwind
 * blocks for D2C stores, multi-vendor marketplaces, admin dashboards,
 * customer accounts, vendor portals, cart, checkout, order management,
 * and ERP integrations.
 *
 * Each body includes:
 *   - Sample arrays for products / cart items / orders / customers / vendors.
 *   - Tabstop placeholders for API endpoints, IDs and routes.
 *   - Loading and empty states where the UI is list-driven.
 *   - No external runtime dependencies beyond React.
 *
 * The auth file (`ecommerce-auth-blocks.code-snippets`) is hand-authored
 * with 40 dedicated snippets (selogin01..20, seregister01..20) and is NOT
 * regenerated by this script.
 *
 * Re-running this script overwrites the four files listed above, so do
 * NOT hand-edit them directly. Edit this generator and re-run:
 *   node scripts/gen_missing_blocks.js
 */
