function writeJson(filename, data) {
  const full = path.join(SNIPPETS_DIR, filename);
  fs.writeFileSync(full, JSON.stringify(data, null, 2) + "\n", "utf8");
  console.log(`wrote ${filename} (${Object.keys(data).length} snippets)`);
}

function buildHooksFile() {
  const out = {};
  REACT_HOOKS.forEach((hook, i) => {
    const { key, snippet } = buildHookSnippet(i + 1, hook);
    out[key] = snippet;
  });
  writeJson("react-hooks-blocks.code-snippets", out);
}

function buildProductFile() {
  const product = {
    ...makeProductGroup({ family: "seproduct", role: "Product",     labels: SEPRODUCT_LABELS }),
    ...makeProductGroup({ family: "sepcard",   role: "ProductCard", labels: SEPCARD_LABELS }),
  };
  writeJson("ecommerce-product-blocks.code-snippets", product);
}

function buildCheckoutFile() {
  const out = {
    ...buildFamily({ family: "secart",     role: "Cart",     labelMap: SECART }),
    ...buildFamily({ family: "secheckout", role: "Checkout", labelMap: SECHECKOUT }),
    ...buildFamily({ family: "seorder",    role: "Order",    labelMap: SEORDER }),
  };
  writeJson("ecommerce-checkout-blocks.code-snippets", out);
}

function buildAdminFile() {
  const out = {
    ...buildFamily({ family: "seaccount", role: "Account", labelMap: SEACCOUNT }),
    ...buildFamily({ family: "seadmin",   role: "Admin",   labelMap: SEADMIN }),
    ...buildFamily({ family: "sevendor",  role: "Vendor",  labelMap: SEVENDOR }),
  };
  writeJson("ecommerce-admin-blocks.code-snippets", out);
}

function main() {
  // Sanity: each label map must have exactly 20 entries.
  for (const [name, list] of Object.entries({ SECART, SECHECKOUT, SEORDER, SEACCOUNT, SEADMIN, SEVENDOR })) {
    if (list.length !== 20) {
      throw new Error(`Family ${name} expected 20 entries, got ${list.length}`);
    }
  }
  buildHooksFile();
  buildProductFile();
  buildCheckoutFile();
  buildAdminFile();
}

main();
