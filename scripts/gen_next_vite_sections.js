function targetFor(framework) {
  return {
    framework,
    img(src, alt, w, h, cls) {
      if (framework === "next") return `<Image src="${src}" alt="${alt}" width={${w}} height={${h}} className="${cls}" />`;
      return `<img src="${src}" alt="${alt}" className="${cls}" />`;
    },
    fillImg(src, alt, cls) {
      if (framework === "next") return `<Image src="${src}" alt="${alt}" fill className="${cls}" />`;
      return `<img src="${src}" alt="${alt}" className="absolute inset-0 h-full w-full ${cls}" />`;
    },
    link(href, cls, children) {
      if (framework === "next") return `<Link href="${href}" className="${cls}">${children}</Link>`;
      return `<Link to="${href}" className="${cls}">${children}</Link>`;
    },
  };
}

