// ---------------------------------------------------------------------------
// Wrap a generated body into a fully-formed snippet body
// ---------------------------------------------------------------------------
function wrap({ framework, componentName, body }) {
  const joined = body.join("\n");
  const needsState = /\b(useState|useEffect)\(/.test(joined);
  const reactHooks = new Set();
  if (/\buseState\(/.test(joined)) reactHooks.add("useState");
  if (/\buseEffect\(/.test(joined)) reactHooks.add("useEffect");
  const needsImage = /<Image\b/.test(joined);
  const needsLink = /<Link\b/.test(joined);

  const imports = [];
  if (framework === "next") {
    if (needsImage) imports.push(`import Image from "next/image";`);
    if (needsLink) imports.push(`import Link from "next/link";`);
    if (reactHooks.size > 0) imports.push(`import { ${Array.from(reactHooks).join(", ")} } from "react";`);
  } else {
    if (reactHooks.size > 0) imports.push(`import { ${Array.from(reactHooks).join(", ")} } from "react";`);
    if (needsLink) imports.push(`import { Link } from "react-router-dom";`);
  }

  const head = [];
  if (framework === "next" && needsState) head.push(`'use client';`, ``);
  if (imports.length > 0) { head.push(...imports); head.push(``); }

  const usesReturn = body.some((line) => /^\s*return\s*\($/.test(line));
  const indented = body.map((l) => (l ? `  ${l}` : ""));

  const fnLines = [];
  fnLines.push(`export default function ${componentName}() {`);
  if (usesReturn) {
    fnLines.push(...indented);
  } else {
    fnLines.push(`  return (`);
    fnLines.push(...body.map((l) => (l ? `    ${l}` : "")));
    fnLines.push(`  );`);
  }
  fnLines.push(`}`);
  fnLines.push(`$0`);

  return [...head, ...fnLines];
}

// ---------------------------------------------------------------------------
// Build snippet entries for a framework
// ---------------------------------------------------------------------------
function buildEntries(framework) {
  const t = targetFor(framework);
  const role = framework === "next" ? "Next" : "Vite";
  const out = {};
  for (const fam of FAMILIES) {
    if (fam.variants.length !== 20) throw new Error(`${fam.key} expected 20 variants, got ${fam.variants.length}`);
    fam.variants.forEach((variant, i) => {
      const idx = i + 1;
      const prefix = `${framework === "next" ? fam.nx : fam.vt}${pad(idx)}`;
      const componentName = `${cap(prefix)}`;
      const body = variant.body(t);
      const lines = wrap({ framework, componentName, body });
      out[`Shelb ${role} — ${fam.label} ${pad(idx)}: ${variant.label} (${prefix})`] = {
        prefix,
        description: `${variant.label}.`,
        body: lines,
      };
    });
  }
  return out;
}

// ---------------------------------------------------------------------------
// Merge into target file (append-only on the requested prefixes)
// ---------------------------------------------------------------------------
function mergeFile(filePath, newEntries) {
  const existing = fs.existsSync(filePath) ? JSON.parse(fs.readFileSync(filePath, "utf8")) : {};
  const existingPrefixes = new Set();
  for (const v of Object.values(existing)) if (v && typeof v.prefix === "string") existingPrefixes.add(v.prefix);

  let added = 0;
  let skipped = 0;
  const merged = { ...existing };
  for (const [k, v] of Object.entries(newEntries)) {
    if (existingPrefixes.has(v.prefix)) { skipped += 1; continue; }
    merged[k] = v;
    added += 1;
  }
  fs.writeFileSync(filePath, JSON.stringify(merged, null, 2) + "\n", "utf8");
  return { added, skipped, total: Object.keys(merged).length };
}

