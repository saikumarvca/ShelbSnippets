function main() {
  for (const [name, list] of Object.entries({ ABOUTS, SERVICES, FEATURES, WHYS })) {
    if (list.length !== 20) throw new Error(`${name} expected 20 entries, got ${list.length}`);
  }

  const existing = fs.existsSync(FILE) ? JSON.parse(fs.readFileSync(FILE, "utf8")) : {};
  const targetFamilies = ["sreabout", "sreservices", "srefeature", "srewhy"];
  const isTarget = (p) => typeof p === "string" && targetFamilies.some((fam) => new RegExp(`^${fam}\\d{2}$`).test(p));

  // Preserve every entry whose prefix is NOT in the target families.
  const preserved = {};
  for (const [key, value] of Object.entries(existing)) {
    if (!isTarget(value && value.prefix)) preserved[key] = value;
  }

  const abouts   = buildFamily({ family: "sreabout",    role: "About",         list: ABOUTS,   prefixWord: "Sreabout" });
  const services = buildFamily({ family: "sreservices", role: "Services",      list: SERVICES, prefixWord: "Sreservices" });
  const features = buildFamily({ family: "srefeature",  role: "Features",      list: FEATURES, prefixWord: "Srefeature" });
  const whys     = buildFamily({ family: "srewhy",      role: "Why Choose Us", list: WHYS,     prefixWord: "Srewhy" });

  const merged = { ...preserved, ...abouts, ...services, ...features, ...whys };
  fs.writeFileSync(FILE, JSON.stringify(merged, null, 2) + "\n", "utf8");
  console.log(`wrote ${path.basename(FILE)} (${Object.keys(merged).length} snippets total)`);
}

main();
