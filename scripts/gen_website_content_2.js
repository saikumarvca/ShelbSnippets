function main() {
  for (const [name, list] of Object.entries({ PRICING, STATS, TEAM, GALLERY, TIMELINE })) {
    if (list.length !== 20) throw new Error(`${name} expected 20 entries, got ${list.length}`);
  }

  const existing = fs.existsSync(FILE) ? JSON.parse(fs.readFileSync(FILE, "utf8")) : {};
  const targetFamilies = ["srepricing", "srestats", "sreteam", "sregallery", "sretimeline"];
  const isTarget = (p) => typeof p === "string" && targetFamilies.some((fam) => new RegExp(`^${fam}\\d{2}$`).test(p));
  const preserved = {};
  for (const [key, value] of Object.entries(existing)) {
    if (!isTarget(value && value.prefix)) preserved[key] = value;
  }

  const srepricing  = buildFamily({ family: "srepricing",  role: "Pricing",  list: PRICING,  prefixWord: "Srepricing"  });
  const srestats    = buildFamily({ family: "srestats",    role: "Stats",    list: STATS,    prefixWord: "Srestats"    });
  const sreteam     = buildFamily({ family: "sreteam",     role: "Team",     list: TEAM,     prefixWord: "Sreteam"     });
  const sregallery  = buildFamily({ family: "sregallery",  role: "Gallery",  list: GALLERY,  prefixWord: "Sregallery"  });
  const sretimeline = buildFamily({ family: "sretimeline", role: "Timeline", list: TIMELINE, prefixWord: "Sretimeline" });

  const merged = { ...preserved, ...srepricing, ...srestats, ...sreteam, ...sregallery, ...sretimeline };
  fs.writeFileSync(FILE, JSON.stringify(merged, null, 2) + "\n", "utf8");
  console.log(`wrote ${path.basename(FILE)} (${Object.keys(merged).length} snippets total)`);
}

main();
