// Main
// ---------------------------------------------------------------------------
function main() {
  for (const [name, list] of Object.entries({ NEWS, TESTS, FAQS, CONTACTS, FOOTERS })) {
    if (list.length !== 20) throw new Error(`${name} expected 20 entries, got ${list.length}`);
  }

  const existing = fs.existsSync(FILE) ? JSON.parse(fs.readFileSync(FILE, "utf8")) : {};
  const targetFamilies = ["srenew", "sretest", "srefaq", "srecontact", "srefooter"];
  const isTarget = (p) => typeof p === "string" && targetFamilies.some((fam) => new RegExp(`^${fam}\\d{2}$`).test(p));

  const preserved = {};
  for (const [key, value] of Object.entries(existing)) {
    if (!isTarget(value && value.prefix)) preserved[key] = value;
  }

  const news      = buildFamily({ family: "srenew",      role: "Newsletter",    list: NEWS,     prefixWord: "Srenew"      });
  const tests     = buildFamily({ family: "sretest",     role: "Testimonials",  list: TESTS,    prefixWord: "Sretest"     });
  const faqs      = buildFamily({ family: "srefaq",      role: "FAQ",           list: FAQS,     prefixWord: "Srefaq"      });
  const contacts  = buildFamily({ family: "srecontact",  role: "Contact",       list: CONTACTS, prefixWord: "Srecontact"  });
  const footers   = buildFamily({ family: "srefooter",   role: "Footer",        list: FOOTERS,  prefixWord: "Srefooter"   });

  const merged = { ...preserved, ...news, ...tests, ...faqs, ...contacts, ...footers };
  fs.writeFileSync(FILE, JSON.stringify(merged, null, 2) + "\n", "utf8");
  console.log(`wrote ${path.basename(FILE)} (${Object.keys(merged).length} snippets total)`);
}

main();
