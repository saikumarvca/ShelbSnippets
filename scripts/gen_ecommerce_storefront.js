function sectionFactory({ component, defaultsArgs, header, listBlock, footer = [] }) {
  return [
    ...propsInterface(component),
    ``,
    `export function ${component}({`,
    ...defaults(defaultsArgs),
    `}: ${component}Props) {`,
    `  return (`,
    `    <section id={sectionId} aria-labelledby={sectionId + "-title"} className={${header.bg ? `"${header.bg} " + className` : `"bg-white text-gray-900 py-14 px-6 " + className`}}>`,
    `      <div className="mx-auto max-w-6xl">`,
    ...header.lines.map((l) => `        ${l}`),
    ...listBlock.map((l) => `        ${l}`),
    ...footer.map((l) => `        ${l}`),
    `      </div>`,
    `    </section>`,
    `  );`,
    `}`,
  ];
}

