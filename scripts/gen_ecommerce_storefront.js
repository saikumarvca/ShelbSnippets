function main() {
  for (const [name, list] of Object.entries({ PCARDS, PRODCARO, HPRODCARO, CATS, OFFERS, DEALS })) {
    if (list.length !== 20) throw new Error(`${name} expected 20 entries, got ${list.length}`);
  }

  const existing = fs.existsSync(FILE) ? JSON.parse(fs.readFileSync(FILE, "utf8")) : {};
  const targetFamilies = ["seprodcaro", "sehprodcaro", "sepcard", "secat", "seoffer", "sedeal"];
  const isTarget = (p) => typeof p === "string" && targetFamilies.some((fam) => new RegExp(`^${fam}\\d{2}$`).test(p));

  const preserved = {};
  for (const [key, value] of Object.entries(existing)) {
    if (!isTarget(value && value.prefix)) preserved[key] = value;
  }

  const seprodcaro  = buildSectionFamily({ family: "seprodcaro",  role: "Product Carousel",            list: PRODCARO,  prefixWord: "Seprodcaro"  });
  const sehprodcaro = buildSectionFamily({ family: "sehprodcaro", role: "Horizontal Product Carousel", list: HPRODCARO, prefixWord: "Sehprodcaro" });
  const secat       = buildSectionFamily({ family: "secat",       role: "Category",                    list: CATS,      prefixWord: "Secat"       });
  const seoffer     = buildSectionFamily({ family: "seoffer",     role: "Offer",                       list: OFFERS,    prefixWord: "Seoffer"     });
  const sedeal      = buildSectionFamily({ family: "sedeal",      role: "Deal",                        list: DEALS,     prefixWord: "Sedeal"      });
  const sepcard     = buildCardFamily(PCARDS);

  const merged = { ...preserved, ...seprodcaro, ...sehprodcaro, ...sepcard, ...secat, ...seoffer, ...sedeal };
  fs.writeFileSync(FILE, JSON.stringify(merged, null, 2) + "\n", "utf8");
  console.log(`wrote ${path.basename(FILE)} (${Object.keys(merged).length} snippets total)`);
}

main();
