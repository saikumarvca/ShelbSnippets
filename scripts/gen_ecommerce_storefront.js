// Renders one product card markup for use inside a carousel/grid.
function productCardJsx({ shell = "card", price = "price", showOld = false, showRating = false, showDiscount = false, addBtn = false, wishlist = false, quickView = false, ratingField = "rating", oldField = "oldPrice", discountField = "discount" } = {}) {
  const widthClass = shell === "snap" ? "snap-start shrink-0 w-56" : "";
  const wrap = shell === "snap"
    ? `<li key={p.id} className="${widthClass} overflow-hidden rounded-2xl border border-gray-200 bg-white">`
    : `<li key={p.id} className="overflow-hidden rounded-2xl border border-gray-200 bg-white">`;
  const badges = [];
  if (showDiscount) badges.push(`{p.${discountField} && <span className="absolute left-3 top-3 z-10 rounded-full bg-red-600 px-2 py-0.5 text-xs font-semibold text-white">{p.${discountField}}</span>}`);
  if (wishlist) badges.push(`<button type="button" aria-label="Wishlist" className="absolute right-3 top-3 z-10 grid h-9 w-9 place-items-center rounded-full bg-white/90 text-gray-500 hover:text-red-600">♡</button>`);
  const rating = showRating
    ? `<p aria-label="rating" className="mt-1 text-xs text-amber-500">{"★★★★★".slice(0, p.${ratingField} ?? 0)}</p>`
    : "";
  const priceBlock = showOld
    ? `<p className="mt-1 flex items-baseline gap-2 text-sm"><span className="font-semibold text-gray-900">{p.${price}}</span>{p.${oldField} && <span className="text-gray-400 line-through">{p.${oldField}}</span>}</p>`
    : `<p className="mt-1 text-sm text-gray-600">{p.${price}}</p>`;
  const quick = quickView ? `<button type="button" aria-label={"Quick view " + p.name} className="absolute inset-x-3 bottom-24 z-10 h-9 rounded-md bg-white/95 text-xs font-semibold text-gray-900 opacity-0 shadow-sm transition group-hover:opacity-100">Quick view</button>` : "";
  const add = addBtn ? `<button type="button" className="mt-2 h-9 w-full rounded-md bg-gray-900 text-xs font-semibold text-white hover:bg-black">Add to cart</button>` : "";
  return `${wrap}<div className="group relative"><div className="aspect-square bg-gray-100">{p.image ? <img src={p.image} alt={p.name} className="h-full w-full object-cover" /> : null}</div>${badges.join("")}${quick}</div><div className="p-3"><p className="text-sm font-semibold text-gray-900">{p.name}</p>${priceBlock}${rating}${add}</div></li>`;
}

