function propsInterface(component, extraFields = []) {
  const base = [
    `  title?: string;`,
    `  subtitle?: string;`,
    `  description?: string;`,
    `  buttonText?: string;`,
    `  buttonHref?: string;`,
    `  imageUrl?: string;`,
    `  className?: string;`,
    `  sectionId?: string;`,
  ];
  return [
    `interface ${component}Props {`,
    ...base,
    ...extraFields,
    `}`,
  ];
}

