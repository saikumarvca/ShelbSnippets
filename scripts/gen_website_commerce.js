function main() {
  for (const [name, list] of Object.entries({ HOMES, BRANDS, COLLECTIONS, BLOGS, CTAS })) {
    if (list.length !== 20) throw new Error(`${name} expected 20 entries, got ${list.length}`);
  }

  const existing = fs.existsSync(FILE) ? JSON.parse(fs.readFileSync(FILE, "utf8")) : {};
  const targetFamilies = ["sehome", "sebrand", "secollection", "sreblog", "srecta"];
  const isTarget = (p) => typeof p === "string" && targetFamilies.some((fam) => new RegExp(`^${fam}\\d{2}$`).test(p));
  const preserved = {};
  for (const [key, value] of Object.entries(existing)) {
    if (!isTarget(value && value.prefix)) preserved[key] = value;
  }

  const sehome       = buildFamily({ family: "sehome",       role: "Storefront Homepage", list: HOMES,       prefixWord: "Sehome"       });
  const sebrand      = buildFamily({ family: "sebrand",      role: "Brand Section",        list: BRANDS,      prefixWord: "Sebrand"      });
  const secollection = buildFamily({ family: "secollection", role: "Collection Section",   list: COLLECTIONS, prefixWord: "Secollection" });
  const sreblog      = buildFamily({ family: "sreblog",      role: "Blog Section",         list: BLOGS,       prefixWord: "Sreblog"      });
  const srecta       = buildFamily({ family: "srecta",       role: "CTA Section",          list: CTAS,        prefixWord: "Srecta"       });

  const merged = { ...preserved, ...sehome, ...sebrand, ...secollection, ...sreblog, ...srecta };
  fs.writeFileSync(FILE, JSON.stringify(merged, null, 2) + "\n", "utf8");
  console.log(`wrote ${path.basename(FILE)} (${Object.keys(merged).length} snippets total)`);
}

main();
