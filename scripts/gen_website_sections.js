// ---------------------------------------------------------------------------
// Build helpers
// ---------------------------------------------------------------------------
function buildFamily({ family, role, list, prefixWord }) {
  const out = {};
  list.forEach((entry, i) => {
    const idx = i + 1;
    const prefix = `${family}${pad(idx)}`;
    const component = `${prefixWord}${pad(idx)}`;
    const body = typeof entry.body === "function" ? entry.body() : entry.body;

    // Hoist any React named-imports from the body and merge them into one
    // top-level import statement together with the default React import.
    const namedReactImports = new Set();
    for (const line of body) {
      const m = line.trim().match(/^import\s+\{([^}]+)\}\s+from\s+["']react["'];?$/);
      if (m) {
        for (const name of m[1].split(",").map((s) => s.trim()).filter(Boolean)) {
          namedReactImports.add(name);
        }
      }
    }
    const topImport = namedReactImports.size > 0
      ? `import React, { ${Array.from(namedReactImports).join(", ")} } from "react";`
      : `import React from "react";`;

    const inner = body.filter((l) => !l.trim().match(/^import\s+\{[^}]+\}\s+from\s+["']react["'];?$/));

    const lines = [
      topImport,
      ``,
      ...propsInterface(component),
    ];

    const componentLines = [
      ``,
      `export function ${component}({`,
      ...defaults(entry.props),
      `}: ${component}Props) {`,
      ...inner.map((l) => (l ? `  ${l}` : "")),
      `}`,
      `$0`,
    ];
    out[`Shelb ${role} — ${entry.label} (${prefix})`] = {
      prefix,
      description: `${entry.label}.`,
      body: [...lines, ...componentLines],
    };
  });
  return out;
}

// ---------------------------------------------------------------------------
// Main: read, merge, write
// ---------------------------------------------------------------------------
function main() {
  for (const [name, list] of Object.entries({ HEROES, CAROS, HCAROS, IMGCAROS })) {
    if (list.length !== 20) throw new Error(`${name} expected 20 entries, got ${list.length}`);
  }

  const existing = JSON.parse(fs.readFileSync(FILE, "utf8"));
  const targetFamilies = ["srehero", "srecaro", "srehcaro", "sreimgcaro"];
  const isTargetPrefix = (p) => typeof p === "string" && targetFamilies.some((fam) => new RegExp(`^${fam}\\d{2}$`).test(p));

  // Preserve every entry whose prefix is NOT in the target families.
  const preserved = {};
  for (const [key, value] of Object.entries(existing)) {
    if (!isTargetPrefix(value && value.prefix)) preserved[key] = value;
  }

  const heroes = buildFamily({ family: "srehero",    role: "Hero",                list: HEROES,    prefixWord: "Srehero" });
  const caros  = buildFamily({ family: "srecaro",    role: "Carousel",            list: CAROS,     prefixWord: "Srecaro" });
  const hcaros = buildFamily({ family: "srehcaro",   role: "Horizontal Carousel", list: HCAROS,    prefixWord: "Srehcaro" });
  const icaros = buildFamily({ family: "sreimgcaro", role: "Image Carousel",      list: IMGCAROS,  prefixWord: "Sreimgcaro" });

  const merged = { ...preserved, ...heroes, ...caros, ...hcaros, ...icaros };
  fs.writeFileSync(FILE, JSON.stringify(merged, null, 2) + "\n", "utf8");
  console.log(`wrote website-sections.code-snippets (${Object.keys(merged).length} snippets total)`);
}

main();
