function pad(n) { return String(n).padStart(2, "0"); }

// ---------------------------------------------------------------------------
// Shared body fragments
// ---------------------------------------------------------------------------
function propsInterface(component) {
  return [
    `interface ${component}Props {`,
    `  title?: string;`,
    `  subtitle?: string;`,
    `  description?: string;`,
    `  buttonText?: string;`,
    `  buttonHref?: string;`,
    `  imageUrl?: string;`,
    `  className?: string;`,
    `  sectionId?: string;`,
    `}`,
  ];
}

function defaults({ title, subtitle, description, buttonText = "Get started", buttonHref = "#", imageUrl = "", sectionId }) {
  return [
    `  title = "\${1:${title}}",`,
    `  subtitle = "\${2:${subtitle}}",`,
    `  description = "\${3:${description}}",`,
    `  buttonText = "\${4:${buttonText}}",`,
    `  buttonHref = "\${5:${buttonHref}}",`,
    `  imageUrl = "\${6:${imageUrl}}",`,
    `  className = "\${7:}",`,
    `  sectionId = "\${8:${sectionId}}",`,
  ];
}

function assemble({ component, sample, props, body }) {
  return [
    `import React from "react";`,
    ``,
    ...propsInterface(component),
    ``,
    ...(sample || []),
    ...(sample && sample.length ? [``] : []),
    `export function ${component}({`,
    ...defaults(props).map((l) => `  ${l.slice(2)}`),
    `}: ${component}Props) {`,
    ...body.map((l) => `  ${l}`),
    `}`,
  ];
}

