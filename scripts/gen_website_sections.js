/**
 * Generator for Shelb Snippets website-sections families:
 *
 *   - srehero01..20      Hero sections
 *   - srecaro01..20      Carousel sections
 *   - srehcaro01..20     Horizontal carousel sections
 *   - sreimgcaro01..20   Image carousel sections
 *
 * The script:
 *   1. Reads snippets/website-sections.code-snippets.
 *   2. Drops every snippet whose prefix is in the families above.
 *   3. Re-builds those 80 entries with hand-authored, label-specific
 *      React + TypeScript + Tailwind bodies following the existing
 *      Shelb style (Props interface with title/subtitle/description/
 *      buttonText/buttonHref/imageUrl/className/sectionId tabstops,
 *      named export, max-w-6xl container, blue-600 accent, $0 anchor).
 *   4. Writes the merged file back. All non-target snippets
 *      (sreabout, sreservices, srefeature, sretest, srefaq, srecontact,
 *      srefooter, …) are preserved untouched.
 *
 *   node scripts/gen_website_sections.js
 */
